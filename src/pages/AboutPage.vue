            <h3>Year 1</h3>
            <p>You are introduced to the basics of every CS degree. The courseworks will hold your hand with well-defined specs and robust testing suites.</p>

            <h4>Programming 1</h4>
            <p>
              Covers the basics of programming from writing your first hello world to the fundamentals of Object-Oriented Programming. We used Java, which I
              remember learning in a single night before the first day, only to realize that was unnecessary.
            </p>

            <h4>Programming 2 (electric boogaloo!)</h4>
            <p>
              Continuation of Programming 1 that introduced us to actual programs with large codebases. Introduction to Git/GitHub and Apache Maven as part of a
              Tetris clone coursework (TetrECS). I strongly remember that we could manipulate the raw scores and upload them to the "multiplayer" server,
              meaning anyone could set a high score illegitimately (I did no such thing :3).
            </p>

            <div class="link-container">
              <a href="https://github.com/yaq1n0/tetrecs" target="_blank" class="link">
